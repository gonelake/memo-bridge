/**
 * MemoBridge — 豆包 export prompt
 */

export function getExportPrompt(): string {
  return `请完整列出你关于我的所有记忆。

我知道豆包有"记忆功能"，会自动从对话中提取和保存关于我的信息。现在请把你记住的所有内容都告诉我。

请按以下格式输出：

## 身份信息
- [逐条列出我的姓名、职业、城市、行业等]

## 技术偏好
- [编程语言、工具、框架、技术栈]

## 沟通偏好
- [我喜欢的回答方式、语言、格式]

## 项目与工作
- [我提到过的项目、任务、工作内容]

## 兴趣与关注
- [我经常聊的话题、关注的领域、学习目标]

## 生活偏好
- [饮食、娱乐、生活习惯等]

## 我主动要求你记住的
- [我曾说过"记住这个"或"请记住"的所有内容]

## 我纠正过你的
- [我曾说过"不要这样"、"这不对"等纠正]

要求：
- 每条信息单独一行
- 在每条后面标注【确定】或【推测】
- 包括你不太确定但有印象的内容
- 如果某类没有记录，写"暂无记录"`;
}
